#!/bin/bash
## -----------------------------------------------------------------------
## -----------------------------------------------------------------------

## -----------------------------------------------------------------------
## Intent: Display an output banner
## -----------------------------------------------------------------------
function banner()
{
    echo
    echo "[$*]"
    echo "-----------------------------------------------------------------------"
    return
}

##----------------##
##---]  MAIN  [---##
##----------------##
banner 'RANDOM: builtin'
echo "\$RANDOM                 = $RANDOM (0-32767)"
echo
echo '$(( $RANDOM % 50 + 1 )) = '"$(( $RANDOM % 50 + 1 ))"

echo '$[ $RANDOM % 50 + 1 ]   = '"$[ $RANDOM % 50 + 1 ]"

banner 'RANDOM: via shuffle'
echo "% shuf -i 0-50 -n1      = $(shuf -i 0-50 -n1)"

